package com.sardo.learnjava.kadai;

public class Math {
    public static Integer add(Integer int1, Integer int2) {
        Integer answer = int1 + int2;
        return answer;
    }

    public static Integer sub(Integer int1, Integer int2) {
        Integer answer = int1 - int2;
        return answer;
    }

    public static Integer plus(Integer int1, Integer int2) {
        Integer answer = int1 * int2;
        return answer;
    }

    public static Integer division(Integer int1, Integer int2) {
        Integer answer = int1 / int2;
        return answer;
    }

    public static String contact(String str1, String str2) {
        String answer = str1 + str2;
        return answer;
    }
}
